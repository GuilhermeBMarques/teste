export const DistintivoCor = (type) => {
  switch (type) {
    case "grass":
      return "#62B957";
    case "fire":
      return "#FD7D24";
    case "water":
      return "#4A90DA";
    case "bug":
      return "#8CB230";
    case "normal":
      return "#9DA0AA";
    case "poison":
      return "#A552CC";
    case "electric":
      return "#EED535";
    case "ground":
      return "#DD7748";
    case "fairy":
      return "#ED6EC7";
    case "fighting":
      return "#D04164";
    case "psychic":
      return "#EA5D60";
    case "rock":
      return "#BAAB82";
    case "ghost":
      return "#556AAE";
    case "ice":
      return "#61CEC0";
    case "dragon":
      return "#0F6AC0";
    case "dark":
      return "#58575F";
    case "steel":
      return "#417D9A";
    case "flying":
      return "#748FC9";
    default:
      return "#A8A878";
  }
};
